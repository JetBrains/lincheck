package tests.custom.set

class SynchronizedMethodSet {
    private val set = mutableSetOf<Int>()

    @Synchronized
    fun add(key: Int): Boolean = set.add(key)

    @Synchronized
    fun remove(key: Int): Boolean = set.remove(key)


    @Synchronized
    fun contains(key: Int): Boolean = set.contains(key)

}